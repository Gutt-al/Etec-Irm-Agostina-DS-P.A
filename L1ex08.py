# Elaborar um algoritmo que leia tres numeros some 10 ao primeiro
# subraia 5 do segundo e multiplique o terceiro por 2
# apos efetuar estas operaçoes mostre o resultado das operaçoes.

n1 = int(input('Digite um numero: '))
n2 = int(input('Digite outro numero: '))
n3 = int(input('Digite outro numero: '))
resultado1 = n1 + 10
resultado2 = n2 - 5
resultado3 = n3 * 2
print(f'O resultado do primeiro numero somado a 10 é: {resultado1}')
print(f'O resultado do segundo numero subtraido por 5 é: {resultado2}')
print(f'O resultado do terceiro numero multiplicado por 2 é: {resultado3}')
