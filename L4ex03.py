# Desenvolva um algoritmo que leia um número e mostre a seguinte mensagem,
# número maior que dez, se ele for maior que dez e menor igual a dez se ele for
# menor ou igual a dez.

n = int(input('Digite um numero: '))
if n > 10:
    print('Esse numero e maior que dez!')
else:
    print('Esse numero e igual ou menor que dez!')
